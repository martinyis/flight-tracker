#!/usr/bin/env node
/**
 * Downloads the mwgg/Airports database and produces a compact TypeScript file
 * containing only commercial airports (3-char IATA codes, non-empty city).
 *
 * Output: frontend/src/data/airports.ts
 */

import { writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));

const SOURCE_URL =
  "https://raw.githubusercontent.com/mwgg/Airports/master/airports.json";

// Names to exclude: helipads, seaplane bases, military-only
const EXCLUDE_PATTERNS = [
  /heliport/i,
  /helipad/i,
  /seaplane/i,
  /military/i,
  /air\s?force\s?base/i,
  /naval\s?air/i,
  /army\s?airfield/i,
  /air\s?base$/i,
  /AFB$/,
];

// Top airports by passenger volume — shown in empty state
const POPULAR_IATA = new Set([
  "JFK", "LAX", "ORD", "ATL", "DFW", "DEN", "SFO", "SEA", "MIA", "BOS",
  "EWR", "LGA", "IAD", "IAH", "PHX", "LAS", "MCO", "MSP", "DTW", "CLT",
  "LHR", "CDG", "AMS", "FRA", "MAD", "BCN", "FCO", "MUC", "IST", "ZRH",
  "NRT", "HND", "ICN", "PEK", "PVG", "HKG", "SIN", "BKK", "TPE", "KIX",
  "SYD", "MEL", "AKL",
  "DXB", "DOH", "GRU", "MEX", "YYZ", "YVR",
  "DEL", "BOM", "MNL", "KUL", "CGK",
]);

async function main() {
  console.log("Downloading airports database...");
  const res = await fetch(SOURCE_URL);
  if (!res.ok) throw new Error(`HTTP ${res.status}`);
  const raw = await res.json();

  const entries = Object.values(raw);
  console.log(`Total entries: ${entries.length}`);

  // Filter: 3-char IATA, has city name, not excluded
  const filtered = entries.filter((a) => {
    if (!a.iata || a.iata.length !== 3) return false;
    if (!a.city) return false;
    if (EXCLUDE_PATTERNS.some((p) => p.test(a.name))) return false;
    return true;
  });

  console.log(`After filtering: ${filtered.length} airports`);

  // Build records: [iata, name, city, country, popular?]
  // Using a tuple array for compactness
  const airports = filtered.map((a) => {
    const rec = {
      iata: a.iata,
      name: a.name,
      city: a.city,
      country: a.country || "",
      popular: POPULAR_IATA.has(a.iata),
    };
    return rec;
  });

  // Sort: popular first, then alphabetical by city
  airports.sort((a, b) => {
    if (a.popular !== b.popular) return a.popular ? -1 : 1;
    return a.city.localeCompare(b.city);
  });

  // Generate TypeScript file with the Airport interface and lookup helpers
  const tsContent = `// Auto-generated by scripts/build-airports.mjs — do not edit manually
// Source: https://github.com/mwgg/Airports
// ${airports.length} airports, ${airports.filter(a => a.popular).length} marked popular

export interface Airport {
  iata: string;
  name: string;
  city: string;
  country: string;
  popular: boolean;
}

/** Country ISO code to display name */
export const COUNTRY_NAMES: Record<string, string> = {
  US:"United States",CA:"Canada",GB:"United Kingdom",DE:"Germany",FR:"France",
  ES:"Spain",IT:"Italy",NL:"Netherlands",BE:"Belgium",CH:"Switzerland",
  AT:"Austria",PT:"Portugal",IE:"Ireland",SE:"Sweden",NO:"Norway",
  DK:"Denmark",FI:"Finland",PL:"Poland",CZ:"Czech Republic",GR:"Greece",
  TR:"Turkey",RU:"Russia",UA:"Ukraine",RO:"Romania",HU:"Hungary",
  HR:"Croatia",RS:"Serbia",BG:"Bulgaria",JP:"Japan",CN:"China",
  KR:"South Korea",TW:"Taiwan",HK:"Hong Kong",SG:"Singapore",TH:"Thailand",
  VN:"Vietnam",MY:"Malaysia",ID:"Indonesia",PH:"Philippines",IN:"India",
  PK:"Pakistan",BD:"Bangladesh",LK:"Sri Lanka",AU:"Australia",NZ:"New Zealand",
  BR:"Brazil",MX:"Mexico",AR:"Argentina",CL:"Chile",CO:"Colombia",PE:"Peru",
  ZA:"South Africa",EG:"Egypt",MA:"Morocco",KE:"Kenya",NG:"Nigeria",
  AE:"United Arab Emirates",SA:"Saudi Arabia",QA:"Qatar",IL:"Israel",
  JO:"Jordan",IS:"Iceland",LU:"Luxembourg",CY:"Cyprus",MT:"Malta",
  PA:"Panama",CR:"Costa Rica",DO:"Dominican Republic",JM:"Jamaica",
  CU:"Cuba",PR:"Puerto Rico",TT:"Trinidad and Tobago",EC:"Ecuador",
  UY:"Uruguay",VE:"Venezuela",BO:"Bolivia",PY:"Paraguay",ET:"Ethiopia",
  TZ:"Tanzania",GH:"Ghana",SN:"Senegal",CI:"Ivory Coast",MU:"Mauritius",
  MG:"Madagascar",BH:"Bahrain",KW:"Kuwait",OM:"Oman",LB:"Lebanon",
  IQ:"Iraq",IR:"Iran",AF:"Afghanistan",UZ:"Uzbekistan",KZ:"Kazakhstan",
  GE:"Georgia",AM:"Armenia",AZ:"Azerbaijan",NP:"Nepal",MM:"Myanmar",
  KH:"Cambodia",LA:"Laos",MN:"Mongolia",FJ:"Fiji",PG:"Papua New Guinea",
  MO:"Macau",BN:"Brunei",MV:"Maldives",BM:"Bermuda",BS:"Bahamas",
  BB:"Barbados",KY:"Cayman Islands",AG:"Antigua and Barbuda",LC:"Saint Lucia",
  VC:"Saint Vincent",GD:"Grenada",AW:"Aruba",CW:"Curacao",SX:"Sint Maarten",
  TC:"Turks and Caicos",VI:"US Virgin Islands",GU:"Guam",AS:"American Samoa",
  MP:"Northern Mariana Islands",DZ:"Algeria",TN:"Tunisia",LY:"Libya",
  SD:"Sudan",RW:"Rwanda",UG:"Uganda",ZM:"Zambia",ZW:"Zimbabwe",BW:"Botswana",
  MZ:"Mozambique",AO:"Angola",CM:"Cameroon",CD:"DR Congo",GA:"Gabon",
  SC:"Seychelles",SK:"Slovakia",SI:"Slovenia",LT:"Lithuania",LV:"Latvia",
  EE:"Estonia",BA:"Bosnia and Herzegovina",ME:"Montenegro",MK:"North Macedonia",
  AL:"Albania",XK:"Kosovo",MD:"Moldova",BY:"Belarus"
};

const airports: Airport[] = ${JSON.stringify(airports)};

export default airports;
`;

  const outPath = resolve(__dirname, "../frontend/src/data/airports.ts");
  writeFileSync(outPath, tsContent, "utf-8");

  const sizeKB = Math.round(Buffer.byteLength(tsContent, "utf-8") / 1024);
  console.log(`Wrote ${airports.length} airports to ${outPath} (${sizeKB} KB)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
